# pobranie danych
num1 = float(input("Podaj pierwsza liczbe "))
num2 = float(input("Podaj druga liczbe "))

# operacje na liczbach
suma = num1 + num2
potegowanie = num1 ** num2

# wyswietlenie wyniku
print(f"Suma Twoich liczb {num1}, {num2} to: {suma}")
print(f"{num1} do potegi {num2} to: {potegowanie}")